package TestPackage;

public class RelationalOperators {
	public static void main(String[] args) {
		
		int a=20,b=15;
		System.out.println("Values of" +" a=" + 20 +" and " +  "b=" + 15);
		 
		//Less than
		System.out.println(a<b);
		
		//Grater than
		System.out.println(a>b);
				
		//Equals to
		System.out.println(a==b);
				
		//Not Equals to
		System.out.println(a!=b);
	}
}
