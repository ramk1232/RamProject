package TestPackage;

public class ArithematicOperators {
	public static void main(String[] args) {
		int a=12;
		float b=2.5f;
		
		//Addition
		System.out.println("Value of a+b="+ (a+b));
		
		//Substraction
		System.out.println("Value of a-b="+ (a-b));
		
		//Multiplication
		System.out.println("Value of a*b="+ (a*b));
		
		//Division
		System.out.println("Value of a/b="+ (a/b));
		
		//Module
		System.out.println("Value of a%b="+ (a%b));
		
		//Increment Operator
		int c=21;
		System.out.println("Value of c=" +c);  
		System.out.println(++c);   //Pre Increment
		System.out.println(c++);   //Post increment
		System.out.println(c);
		
		
		//Decrement Operator
		int d=21;
		System.out.println("Value of d=" +d);  
		System.out.println(--d);   //Pre Decrement
		System.out.println(d--);   //Post Decrement
		System.out.println(d); 
		
		//Assignment Oprator used to assign values to variables
		int ram;
	    ram=007; 
	    System.out.println("7 is assigned to variable ram " +ram);
		
						
				
				
	}
	
 
}
